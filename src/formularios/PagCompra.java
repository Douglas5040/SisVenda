    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        CompraDao objDao = new CompraDao();
        ClienteDao objCliDao = new ClienteDao();
        ContasDao objContaDao = new ContasDao();
        ContasCtrl objContaCtrl = new ContasCtrl();
        
        String valTotal = jlValorTotal.getText();
        String data, hora;
        Date dataSistema = new Date();
        SimpleDateFormat formato = new SimpleDateFormat("yyyy/MM/dd");
        data = (formato.format(dataSistema));
        Calendar now = Calendar.getInstance();
        hora = (String.format("%1$tH:%1$tM:%1tS", now));
        valTotal = valTotal.replace(",", ".");

        objDao.alterar(SisPrinc.codComp, idCli, Double.valueOf(valTotal), data,  hora, "NA CONTA");
        objCliDao.alterarSituation(idCli, jNomeClie.getText(), "DEVENDO");
        
        if(hasConta){
            objContaDao.alterar(idCli, Double.valueOf(valTotal)+objContaDao.selectValor(idCli),
                                    jfData.getText().substring(6,10)+
                                    jfData.getText().substring(2,6)+
                                    jfData.getText().substring(0,2),dataOpen, "DEVENDO");
        }else{
            objContaCtrl.setCod_cli(idCli);
            objContaCtrl.setData_pag(jfData.getText().substring(6,10)+
                                     jfData.getText().substring(2,6)+
                                     jfData.getText().substring(0,2));

            objContaCtrl.setValor(Double.valueOf(valTotal));
            objContaCtrl.setStatus("DEVENDO");
            objContaDao.inserir(objContaCtrl);
        }
            
            
            JOptionPane.showMessageDialog(null, "VENDA REALIZADA COM SUCESSO!!!");
        SisPrinc.clearTablePro = true;
        jlValorTotal.setText("00,00");
        lStatus.setText("STATUS");
        jNomeClie.setText("");
        jcombNome.removeAllItems();
        dispose();
    }//GEN-LAST:event_jButton1ActionPerformed


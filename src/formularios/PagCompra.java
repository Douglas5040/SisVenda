    private void jNomeClieKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jNomeClieKeyPressed
        ClienteDao objDao = new ClienteDao();
        ContasDao objCotaDao = new ContasDao();
        if(evt.getKeyCode()==KeyEvent.VK_ENTER && !jNomeClie.getText().equals("")){
            ResultSet objRs = objDao.selecionarUser(jNomeClie.getText());
            jcombNome.removeAllItems();
            try {
                while(objRs.next()){jcombNome.addItem(objRs.getString("nomeCli"));}
                while(objRs.previous()){}

                objRs.next();
                idCli = objRs.getInt("id");
            } catch (SQLException ex) {
                //JOptionPane.showMessageDialog(null, "Cliente não encontrado "+ex);
            }
            //verificando conta de cliente
            hasConta = objCotaDao.checkContaCli(idCli);
            if(hasConta){
                //JOptionPane.showMessageDialog(null, "asdfasdasdasd ");
                ResultSet objRsConta = objCotaDao.selectConta(idCli);
                try {
                    objRs.first();
                    dataOpen = objRsConta.getString("data_open");
                    jfData.setText(objRsConta.getString("data_pag"));
                    jlValconta.setText(objRsConta.getString("valor"));
                    
                } catch (SQLException ex) {
                    JOptionPane.showMessageDialog(null, "erro ao fazer cunsulta de COntas "+ex);
                }
                jNomeClie.setNextFocusableComponent(jfData);
                jNomeClie.nextFocus();
                jfData.selectAll();
                
            }else{
                //JOptionPane.showMessageDialog(null, "--------- ");
                jNomeClie.setNextFocusableComponent(jfData);
                jNomeClie.nextFocus();
                jfData.selectAll();
                jlValconta.setText("00,00");
            }
            jNomeClie.setText(jcombNome.getItemAt(0));
            lStatus.setText(objDao.selecionarStatusUser(String.valueOf(idCli)));
            jcombNome.setNextFocusableComponent(jNomeClie);
            jcombNome.nextFocus();

            if(jcombNome.getItemCount()<1){
                if(JOptionPane.showConfirmDialog(null, "Deseja Cadastra-lo?", "CLIENTE NÃO ENCONTRADO", 0)==0){
                    GerirCliente cadCli = new GerirCliente();
                    cadCli.show();
                }
            }
        }
    }//GEN-LAST:event_jNomeClieKeyPressed


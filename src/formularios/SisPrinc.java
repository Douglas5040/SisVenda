    private void jtCodProKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jtCodProKeyPressed
        ProdutoDao objDao = new ProdutoDao();
        boolean isAddPro = false;
        
    if(evt.getKeyCode()==KeyEvent.VK_ENTER){
        if( Integer.parseInt(jtCodPro.getText().replace("", "0")) > 0 && objDao.verificarPro(jtCodPro.getText())){
            if(objDao.qtdPro(jtCodPro.getText()) != -1 || objDao.qtdPro(jtCodPro.getText()) != 0){           
              for(int x=0; x < jTablePro.getRowCount(); x++){
                if(jTablePro.getValueAt(x, 0).equals(jtCodPro.getText()) ){
                    isAddPro = true;
                    posiPro = x;

                    ResultSet objRs = objDao.selecionarPro(jtCodPro.getText());
                    try {
                        objRs.first();
                        qtdPro = objRs.getInt("ESTOQ_ATUAL");
                    } catch (SQLException ex) {
                        JOptionPane.showMessageDialog(null, "ERRO: PRODUTO NÃO ENCONTRADO "+ex);
                    }
                }
              }
                if(!isAddPro){     
                    jcombNomePro.removeAllItems();
                    ResultSet objRs = objDao.selecionarPro(jtCodPro.getText());

                      try {

                           while(objRs.next()){jcombNomePro.addItem(objRs.getString("NOME"));}
                           while(objRs.previous()){}
                           objRs.first();
                           jtValorUnit.setText(objRs.getString("PRECO_VAREJO"));
                           qtdPro = objRs.getInt("ESTOQ_ATUAL");

                      } catch (SQLException ex) {
                          JOptionPane.showMessageDialog(null, "ERRO: PRODUTO NÃO ENCONTRADO "+ex);
                          jbAdd.setNextFocusableComponent(jtCodPro);
                          jbAdd.nextFocus();
                          jtCodPro.selectAll();
                      }
                      jtCodPro.setNextFocusableComponent(jtQTD);
                      jtQTD.selectAll();
                      jtCodPro.nextFocus();
              }else{
                    if(JOptionPane.showConfirmDialog(jPanel6, "Deseja adicionar mais?", "PRODUTO JA ADICIONADO", 0)==0){
                        jtValorUnit.setText(jTablePro.getValueAt(posiPro, 2).toString());
                        jNomePro.setText(jTablePro.getValueAt(posiPro, 1).toString());
                        jtQTD.setText(jTablePro.getValueAt(posiPro, 3).toString());
                        jTablePro.setRowSelectionInterval(posiPro, posiPro);
                        wasEdit = true;

                        jtCodPro.setNextFocusableComponent(jtQTD);
                        jtCodPro.nextFocus();
                        jtQTD.selectAll();
                    }else{
                        jbAdd.setNextFocusableComponent(jtCodPro);
                        jbAdd.nextFocus();
                        jtCodPro.selectAll();
                        wasEdit = false;
                    }
                }
            }else {
                JOptionPane.showMessageDialog(null, "PRODUTO EM FALTA NO ESTOQUE");
                jbAdd.setNextFocusableComponent(jtCodPro);
                jbAdd.nextFocus();
                jtCodPro.selectAll();
            }
        }else{
            JOptionPane.showMessageDialog(null, "PRODUTO NÃO ENCONTRADO");
            jbAdd.setNextFocusableComponent(jtCodPro);
            jbAdd.nextFocus();
            jtCodPro.selectAll();
        }
            
      }
      if(evt.getKeyCode()==KeyEvent.VK_TAB || evt.getKeyCode()==KeyEvent.VK_9){
          jtCodPro.setNextFocusableComponent(jtNomePro);
          jtCodPro.nextFocus();
          jtNomePro.selectAll();
      }
    }//GEN-LAST:event_jtCodProKeyPressed


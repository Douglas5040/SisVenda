    private void jbSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbSalvarActionPerformed
        
               forCtrl.setBairro(edtBairro.getText());
               forCtrl.setCelular(edtCelFor.getText());
               
                String[] cep = edtCepFor.getText().split("-");
                if(edtCepFor.getText().equals("") || edtCepFor.getText().equals("-"))forCtrl.setCep(0);
                else forCtrl.setCep(Integer.parseInt(cep[0]+cep[1]));
               
                forCtrl.setCidade(edtCidade.getText());
               forCtrl.setCpf_cnpj(Integer.parseInt(edtCnpjCpfFor.getText()));
               if(radioCnpj.isSelected()) forCtrl.setCpf_or_cnpj("CNPJ");
               if(radioCpf.isSelected()) forCtrl.setCpf_or_cnpj("CPF");
               forCtrl.setEmail(edtEmailFor.getText());
               forCtrl.setEnder(edtEnder.getText());
               forCtrl.setEstado(edtEstado.getText());
               forCtrl.setNome_fantazia(edtNomeFan.getText());
               forCtrl.setObs(txtObs.getText());
               forCtrl.setRazao_social(edtRazaoSoci.getText());
               forCtrl.setTelefone(edtTelResiFor.getText());
        //Salvar
        if(!isEditFor){
            if(forDao.selectCodForneceID(edtCnpjCpfFor.getText()) < 1){

                forDao.inserir(forCtrl);
                
                edtNomeFan.setText("");
                edtRazaoSoci.setText("");
                edtCnpjCpfFor.setText("");
                edtCelFor.setText("");
                edtTelResiFor.setText("");
                edtEmailFor.setText("");
                edtCepFor.setText("");
                edtEnder.setText("");
                edtBairro.setText("");
                edtCidade.setText("");
                edtEstado.setText("");
                txtObs.setText("");
                
                jbSalvar.setNextFocusableComponent(edtNomeFan);
                jbSalvar.nextFocus();
                
            }else JOptionPane.showMessageDialog(null, "Fornecedor j√° existente");
         //editar
        }else{
            if(forDao.selectCodForneceID(edtCnpjCpfFor.getText()) < 1){
                forDao.alterar(forCtrl);
                
                edtNomeFan.setText("");
                edtRazaoSoci.setText("");
                edtCnpjCpfFor.setText("");
                edtCelFor.setText("");
                edtTelResiFor.setText("");
                edtEmailFor.setText("");
                edtCepFor.setText("");
                edtEnder.setText("");
                edtBairro.setText("");
                edtCidade.setText("");
                edtEstado.setText("");
                txtObs.setText("");
                
                jbSalvar.setNextFocusableComponent(edtNomeFan);
                jbSalvar.nextFocus();
             
                isEditFor = false;
            }else JOptionPane.showMessageDialog(null, "Fornecedor j√° existente");
        }
        
    }//GEN-LAST:event_jbSalvarActionPerformed


    private void jbEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbEditarActionPerformed
        if(jTableFor.getSelectedRow() != -1){
            ResultSet rsFor = forDao.selectFornece(objTM.getValueAt(jTableFor.getSelectedRow(), 0).toString());
            
            try {
                rsFor.first();
                    forCtrl.setID(Integer.parseInt(rsFor.getString("ID")));
                    forCtrl.setCodigo(rsFor.getString("CODIGO"));
                    forCtrl.setCep(Integer.parseInt(rsFor.getString("CEP")));
                    forCtrl.setBairro(rsFor.getString("BAIRRO"));
                    forCtrl.setCelular(rsFor.getString("CELULAR"));
                    forCtrl.setCidade(rsFor.getString("CIDADE"));
                    forCtrl.setCpf_cnpj(Integer.parseInt(rsFor.getString("CPF_CNPJ")));
                    if(rsFor.getString("CPF_OR_CNPJ").equals("CPF")) forCtrl.setCpf_or_cnpj("CPF");
                    if(rsFor.getString("CPF_OR_CNPJ").equals("CNPJ")) forCtrl.setCpf_or_cnpj("CNPJ");
                    forCtrl.setEmail(rsFor.getString("EMAIL"));
                    forCtrl.setEnder(rsFor.getString("ENDER"));
                    forCtrl.setEstado(rsFor.getString("ESTADO"));
                    forCtrl.setNome_fantazia(rsFor.getString("NOME_FANTAZIA"));
                    forCtrl.setObs(rsFor.getString("OBS"));
                    forCtrl.setRazao_social(rsFor.getString("RAZAO_SOCIAL"));
                    forCtrl.setTelefone(rsFor.getString("TELEFONE"));
               
                    edtNomeFan.setText(forCtrl.getNome_fantazia());
                    edtRazaoSoci.setText(forCtrl.getRazao_social());
                    edtCnpjCpfFor.setText(""+forCtrl.getCpf_cnpj());
                    if(forCtrl.getCpf_or_cnpj().equals("CPF")) radioCpf.setSelected(true);
                    if(forCtrl.getCpf_or_cnpj().equals("CNPJ")) radioCnpj.setSelected(true);
                    edtCelFor.setText(forCtrl.getCelular());
                    edtTelResiFor.setText(forCtrl.getTelefone());
                    edtEmailFor.setText(forCtrl.getEmail());
                    edtCepFor.setText(""+forCtrl.getCep());
                    edtEnder.setText(forCtrl.getEnder());
                    edtBairro.setText(forCtrl.getBairro());
                    edtCidade.setText(forCtrl.getCidade());
                    edtEstado.setText(forCtrl.getEstado());
                    txtObs.setText(forCtrl.getObs());
                
                    jtpFor.setSelectedIndex(0);
                    
                    jbEditar.setNextFocusableComponent(edtNomeFan);
                    jbEditar.nextFocus();
                    edtNomeFan.selectAll();
                    
                    isEditFor = true;
                
            } catch (SQLException ex) {
                Logger.getLogger(GerirFornecedor.class.getName()).log(Level.SEVERE, null, ex);
                JOptionPane.showMessageDialog(null, "Erro na consulda de Fornecedores, "+ex);
            }
            
        }
           
    }//GEN-LAST:event_jbEditarActionPerformed


    private void jbSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbSalvarActionPerformed
        switch(SisPrinc.codParametro){
            case 1:     
                if(!isEditParam){
                    try {
                        if(!mDao.selectMarca(jtNome.getText()).first()){
                            mCtrl.setID(0);
                            mCtrl.setNome(jtNome.getText());
                            mCtrl.setDescri(jtaDescri.getText());

                            mDao.inserir(mCtrl);
                            JOptionPane.showMessageDialog(null, "Marca Salva com sucesso!!!");

                            jtNome.setText("");
                            jtAbrev.setText("");
                            jtaDescri.setText("");
                            
                            jtaDescri.setNextFocusableComponent(jtNome);
                            jtaDescri.nextFocus();

                        }else JOptionPane.showMessageDialog(null, "Marca já existente!!!");
                    } catch (SQLException ex) {
                        Logger.getLogger(GerirParametros.class.getName()).log(Level.SEVERE, null, ex);
                    }
                }else{
                    
                    mCtrl.setNome(jtNome.getText());
                    mCtrl.setDescri(jtaDescri.getText());
                    if(mDao.alterar(mCtrl)){
                        JOptionPane.showMessageDialog(null, "Marca Alterada com sucesso!!!");
                        isEditParam = false;
                        jtNome.setText("");
                        jtAbrev.setText("");
                        jtaDescri.setText("");
                    }
                }
            break;
            case 2:      
                if(!isEditParam){
                    try {
                        if(!cDao.selecionarCatego(jtNome.getText()).first()){
                            cCtrl.setID(0);
                            cCtrl.setNome(jtNome.getText());
                            cCtrl.setDescri(jtaDescri.getText());

                            cDao.inserir(cCtrl);
                            JOptionPane.showMessageDialog(null, "Categoria Salva com sucesso!!!");

                            jtNome.setText("");
                            jtAbrev.setText("");
                            jtaDescri.setText("");  
                            
                            jtaDescri.setNextFocusableComponent(jtNome);
                            jtaDescri.nextFocus();  
                        }else JOptionPane.showMessageDialog(null, "Categoria já existente!!!");
                    } catch (SQLException ex) {
                        Logger.getLogger(GerirParametros.class.getName()).log(Level.SEVERE, null, ex);
                    }
                }else{
                    cCtrl.setNome(jtNome.getText());
                    cCtrl.setDescri(jtaDescri.getText());
                    if(cDao.alterar(cCtrl)){
                        JOptionPane.showMessageDialog(null, "Categoria Alterada com sucesso!!!");
                        isEditParam = false;
                        jtNome.setText("");
                        jtAbrev.setText("");
                        jtaDescri.setText("");
                    }
                }
                
            break;
            case 3:      
                if(!isEditParam){
                    try {
                        if(!umDao.selecionarUndMed(jtNome.getText()).first()){
                    
                            umCtrl.setID(0);
                            umCtrl.setAbrev(jtAbrev.getText());
                            umCtrl.setNome(jtNome.getText());

                            umDao.inserir(umCtrl);
                            JOptionPane.showMessageDialog(null, "Unidade de Medida Salva com sucesso!!!");

                            jtNome.setText("");
                            jtAbrev.setText("");
                            jtaDescri.setText("");
                            
                            jtaDescri.setNextFocusableComponent(jtAbrev);
                            jtaDescri.nextFocus();
                        }else JOptionPane.showMessageDialog(null, "Unidade de Medida já existente!!!");
                    } catch (SQLException ex) {
                        Logger.getLogger(GerirParametros.class.getName()).log(Level.SEVERE, null, ex);
                    }
                }else{
                    umCtrl.setAbrev(jtAbrev.getText());
                    umCtrl.setNome(jtNome.getText());
                    if(umDao.alterar(umCtrl)){
                        JOptionPane.showMessageDialog(null, "Unidade de Medida Alterada com sucesso!!!");
                        isEditParam = false;
                        jtNome.setText("");
                        jtAbrev.setText("");
                        jtaDescri.setText("");
                    } 
                }
                
            break;
            default : JOptionPane.showMessageDialog(null, "Valor de Parametro não encontrado!!!");
        }
        
    }//GEN-LAST:event_jbSalvarActionPerformed

    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        if(jTableParam.getSelectedRow() != -1){
            isEditParam = true;
            switch(SisPrinc.codParametro){
                case 1:
                    mCtrl.setID(Integer.parseInt(objTM.getValueAt(jTableParam.getSelectedRow(), 0).toString()));
                    mCtrl.setNome(objTM.getValueAt(jTableParam.getSelectedRow(), 1).toString());
                    mCtrl.setDescri(objTM.getValueAt(jTableParam.getSelectedRow(), 2).toString());
                    
                    jtNome.setText(mCtrl.getNome());
                    jtaDescri.setText(mCtrl.getDescri());
                    jtAbrev.setNextFocusableComponent(jtNome);
                    jtAbrev.nextFocus();
                    jtNome.selectAll();
                break;
                case 2:
                    cCtrl.setID(Integer.parseInt(objTM.getValueAt(jTableParam.getSelectedRow(), 0).toString()));
                    cCtrl.setNome(objTM.getValueAt(jTableParam.getSelectedRow(), 1).toString());
                    cCtrl.setDescri(objTM.getValueAt(jTableParam.getSelectedRow(), 2).toString());
                    
                    jtNome.setText(cCtrl.getNome());
                    jtaDescri.setText(cCtrl.getDescri());
                    jtAbrev.setNextFocusableComponent(jtNome);
                    jtAbrev.nextFocus();
                    jtNome.selectAll();
                break;
                case 3:
                    umCtrl.setID(Integer.parseInt(objTM.getValueAt(jTableParam.getSelectedRow(), 0).toString()));
                    umCtrl.setAbrev(objTM.getValueAt(jTableParam.getSelectedRow(), 1).toString());
                    umCtrl.setNome(objTM.getValueAt(jTableParam.getSelectedRow(), 2).toString());
                    
                    jtAbrev.setText(umCtrl.getAbrev());
                    jtNome.setText(umCtrl.getNome());
                    jtPesqParam.setNextFocusableComponent(jtAbrev);
                    jtPesqParam.nextFocus();
                    jtAbrev.selectAll();
                break;
            }

            jTabbedPane1.setSelectedIndex(0);
        }else JOptionPane.showMessageDialog(null, "Selecione um item para EDITAR!!!");
    }//GEN-LAST:event_jButton4ActionPerformed


    public void selectParam(){
        objTM.setRowCount(0);
        System.out.println("codParametro select: "+SisPrinc.codParametro);
        switch(SisPrinc.codParametro){
            case 1:
                ResultSet marcaRs = mDao.selectMarca(jtPesqParam.getText());
                try{
                    while(marcaRs.next()){
                        String linha[] = {
                            marcaRs.getString("ID"),
                            marcaRs.getString("NOME"),
                            marcaRs.getString("DESCRI")
                        };
                        System.out.println(Arrays.toString(linha));
                           objTM.addRow(linha);
                        
                    }
                }catch(SQLException ex){
                    JOptionPane.showMessageDialog(null, "Marca Não encontrada, "+ex);
                }
            break;
            case 2:
                ResultSet cateRs = cDao.selecionarCatego(jtPesqParam.getText());
                try{
                    while(cateRs.next()){
                        String linha[] = {
                            cateRs.getString("ID"),
                            cateRs.getString("NOME"),
                            cateRs.getString("DESCRI")
                        };
                        System.out.println(Arrays.toString(linha));
                           objTM.addRow(linha);
                        
                    }
                }catch(SQLException ex){
                    JOptionPane.showMessageDialog(null, "Categoria Não encontrada, "+ex);
                }
            break;
            case 3:
                ResultSet umRs = umDao.selecionarUndMed(jtPesqParam.getText());
                try{
                    while(umRs.next()){
                        String linha[] = {
                            umRs.getString("ID"),
                            umRs.getString("ABREV"),
                            umRs.getString("NOME")
                        };
                        System.out.println(Arrays.toString(linha));
                           objTM.addRow(linha);
                        
                    }
                }catch(SQLException ex){
                    JOptionPane.showMessageDialog(null, "Unidade de Medida Não encontrada, "+ex);
                }
            break;

        }
    }

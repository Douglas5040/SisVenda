    private void formWindowGainedFocus(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowGainedFocus
      if(jlNomeCli.getText().equals("Nome")){
        ContasDao objContaDao = new ContasDao();
        RelatorioDAO objDAORel = new RelatorioDAO();
        DefaultTableModel objTableM = (DefaultTableModel) jtCompCli.getModel();
        ResultSet objRs = objContaDao.selectConta(SisPrinc.codCli);
        
            try {
                objRs.first();
                    dataOP = objRs.getString("data_open");
                    codConta = Integer.parseInt(objRs.getString("cod_conta"));
                    jlValorDev.setText(objRs.getString("valor"));
                

            } catch (SQLException ex) { JOptionPane.showMessageDialog(null, "erro ao fazer cunsulta de Conta "+ex);}
            jlNomeCli.setText(SisPrinc.nomeCli);
            
            ResultSet objRsVend = objDAORel.consultVendaCLi(dataOP,SisPrinc.codCli);
                try {
                   objTableM.setRowCount(0);
                   while(objRsVend.next()){
                       String linha[] = {
                            objRsVend.getString("dataCompra").substring(8,10)+"/"+
                            objRsVend.getString("dataCompra").substring(5,7)+"/"+
                            objRsVend.getString("dataCompra").substring(0,4),
                           
                            objRsVend.getString("horaCompra"),
                            objRsVend.getString("codigo"),
                            objRsVend.getString("valorTotal")

                       };
                       objTableM.addRow(linha);
                }} catch (SQLException ex) {
                       JOptionPane.showMessageDialog(null, "erro ao fazer cunsulta de Venda Detalhada"+ex);
                }
      }
            
    }//GEN-LAST:event_formWindowGainedFocus


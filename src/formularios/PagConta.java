    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        CompraDao objDao = new CompraDao();
        ContasDao objContaDao = new ContasDao();
        ClienteDao objDaoCli = new  ClienteDao();
        DefaultTableModel objTableM = (DefaultTableModel) jtCompCli.getModel();

        
        objContaDao.deletar(codConta, SisPrinc.codCli);
        objDaoCli.alterarSituation(SisPrinc.codCli, SisPrinc.nomeCli, "NORMAL");
        
        for(int x=0;x<objTableM.getRowCount();x++){
            objDao.alterar(Integer.parseInt(objTableM.getValueAt(x, 2).toString()), SisPrinc.codCli, 
                   Double.valueOf(objTableM.getValueAt(x, 3).toString()), objTableM.getValueAt(x, 0).toString().substring(6,10)+
                                                                          objTableM.getValueAt(x, 0).toString().substring(2,6)+
                                                                          objTableM.getValueAt(x, 0).toString().substring(0,2),
                   objTableM.getValueAt(x, 1).toString(), "CONTA PAGA");
        }
        dispose();
        JOptionPane.showMessageDialog(null, "CONTA PAGA COM SUCESSO!!!");
        objTableM.setRowCount(0);
        jlValorDev.setText("00,00");
        dataOP = null;
        jlNomeCli.setText("Nome");
        
    }//GEN-LAST:event_jButton1ActionPerformed

